/**
 * Adds two numbers.
 * @param {number} a
 * @param {number} b
 * @returns {number} Sum of a and b
 */
export function add(a, b) {
  return a + b;
}

/**
 * Multiplies two numbers.
 * @param {number} a
 * @param {number} b
 * @returns {number} Product of a and b
 */
export function multiply(a, b) {
  return a * b;
}

